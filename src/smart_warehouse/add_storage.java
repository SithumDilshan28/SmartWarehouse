     public final void autoID() {
        
         try {
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/smartwarehouse","root","");
            Statement s = con.createStatement();

            ResultSet rs = s.executeQuery("select Max(storageID) from storage");
            rs.next();

            rs.getString("Max(storageID)");


            if(rs.getString("Max(storageID)") == null)
            {

                storageNo.setText("S-001");
            }
            else
            {
                Long storageID = Long.parseLong(rs.getString("Max(storageID)").substring(2,rs.getString("Max(storageID)").length()));
                storageID++;
                storageNo.setText(String.format("S-"+"%03d", storageID));

            }


        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

