    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
       
        l1.setText("");
        
        if(sDate.getDate()== null){
        l1.setText("Required Date");
        
        }
        
        else if(sDate.getDate()== null){
            l1.setText("Required Date");
        }
       
        else{    
        try {
                // TODO add your handling code here:
                String storageID = storageNo.getText();
                String userID = (String) id.getSelectedItem();
                SimpleDateFormat dat = new SimpleDateFormat ("yyyy-MM-dd");
                String date = dat.format (sDate.getDate());
                String catergory = (String) type.getSelectedItem();
                String space = (String) storageSpace.getSelectedItem();
                String duration = (String) time.getSelectedItem();
                String quantity = (String) storageQuantity.getSelectedItem();
                String status = stat.getText();
                storageNo.requestFocus();

                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/smartwarehouse","root","");
                PreparedStatement pst = con.prepareStatement("insert into storage (storageID,userID,date,catergory,space,duration,quantity,Status)values(?,?,?,?,?,?,?,?)");
                
                pst.setString(1, storageID);
                pst.setString(2, userID);
                pst.setString(3, date);
                pst.setString(4, catergory);
                pst.setString(5, space);
                pst.setString(6, duration);
                pst.setString(7, quantity);
                pst.setString(8, status);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this,"Storage Added Successfully.");

                autoID();
                
                id.setSelectedItem("");
                sDate.setDate(null);
                type.setSelectedItem("");
                storageSpace.setSelectedItem("");
                time.setSelectedItem("");
                storageQuantity.setSelectedItem(""); 


               }catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jLabel7MouseClicked

